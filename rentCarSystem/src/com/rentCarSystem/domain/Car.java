package com.rentCarSystem.domain;

import java.io.Serializable;

public class Car implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String carNumber;// 汽车车号
	private String carType;// 汽车型号
	private String color;// 汽车颜色
	private String price;// 汽车价格
	private String rentPrice;// 汽车租金
	private String deposit;// 汽车押金
	private String isRenting;// 租用情况
	private String description;// 汽车简介

	public String getCarNumber() {
		return carNumber;
	}

	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}

	public String getCarType() {
		return carType;
	}

	public void setCarType(String carType) {
		this.carType = carType;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	
	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	

	public String getRentPrice() {
		return rentPrice;
	}

	public void setRentPrice(String rentPrice) {
		this.rentPrice = rentPrice;
	}

	public String getDeposit() {
		return deposit;
	}

	public void setDeposit(String deposit) {
		this.deposit = deposit;
	}

	public String getIsRenting() {
		return isRenting;
	}

	public void setIsRenting(String isRenting) {
		this.isRenting = isRenting;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public String toString() {
		return "Car [carNumber=" + carNumber + ", carType=" + carType
				+ ", color=" + color + ", price=" + price + ", rentPrice="
				+ rentPrice + ", deposit=" + deposit + ", isRenting="
				+ isRenting + ", description=" + description + "]";
	}
	
}
